app.get('/api/products', async (req, res) => {
  try {
    const {
      page = 1,
      limit = 10,
      category,
      search
    } = req.query;
    
    let query = db('products')
      .where('is_active', true);
    
    // –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    if (category) {
      const categoryObj = await db('categories').where({ slug: category }).first();
      if (categoryObj) {
        query = query
          .join('product_categories', 'products.id', 'product_categories.product_id')
          .where('product_categories.category_id', categoryObj.id);
      }
    }
    
    // –ü–æ–∏—Å–∫ –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é –∏–ª–∏ –æ–ø–∏—Å–∞–Ω–∏—é
    if (search) {
      query = query.where(function() {
        this.where('products.name', 'ilike', `%${search}%`)
            .orWhere('products.description', 'ilike', `%${search}%`);
      });
    }
    
    // –ü–æ–¥—Å—á–µ—Ç –æ–±—â–µ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Ç–æ–≤–∞—Ä–æ–≤
    const countQuery = query.clone();
    const [{ count }] = await countQuery.count('products.id as count');
    
    // –ü–æ–ª—É—á–µ–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ —Å –ø–∞–≥–∏–Ω–∞—Ü–∏–µ–π
    const products = await query
      .select('products.*')
      .orderBy('products.name')
      .offset((page - 1) * limit)
      .limit(limit);
    
    res.json({
      success: true,
      products,
      pagination: {
        total: parseInt(count),
        page: parseInt(page),
        limit: parseInt(limit),
        pages: Math.ceil(count / limit)
      }
    });
  } catch (error) {
    console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ç–æ–≤–∞—Ä–æ–≤:', error);
    res.status(500).json({
      error: '–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ç–æ–≤–∞—Ä–æ–≤'
    });
  }
});

